package swai.location.data.yelp;

public class Region {

	private Center center;
	private Span span;

	public Region() {
	}

	public Center getCenter() {
		return center;
	}

	public void setCenter(Center center) {
		this.center = center;
	}

	public Span getSpan() {
		return span;
	}

	public void setSpan(Span span) {
		this.span = span;
	}
}
